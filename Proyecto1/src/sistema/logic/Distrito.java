package sistema.logic;

/**
 *
 * @author 50663
 */
public class Distrito {
    String nombre;

    public Distrito(String nombre) {
        this.nombre = nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    @Override
    public String toString() {
        return "Distrito{" + "nombre=" + nombre + '}';
    }

  
    
    
    
}
